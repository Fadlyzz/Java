    private void namabarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_namabarangActionPerformed
String nama=(String)namabarang.getSelectedItem();
switch (nama){
    case "TVLED01":
        namba.setText("TV LED");
        harga.setText("1000000");
        break;
    case "COMPT02":
        namba.setText("Fullset Komputer");
        harga.setText("9000000");
        break;
    case "LAPTOP":
        namba.setText("Laptop Asus");
        harga.setText("5000000");
        break;
    case "HARDDISK":
        namba.setText("Harddisk");
        harga.setText("900000");
        break;
    case "SSD":
        namba.setText("SSD VGen 1 Tb");
        harga.setText("1200000");
        break;
    case "PILIH BARANG":
        namba.setText("-");
        harga.setText("Belum Memilih Barang");
        break;
}
    }//GEN-LAST:event_namabarangActionPerformed

