    private void bayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bayarActionPerformed
int jmlbayar=Integer.parseInt(jbayar.getText());
int jmlharga=Integer.parseInt(jharga.getText());
if (jmlbayar<jmlharga){
    kembalian.setText("Uang Anda Kurang!!");
    JOptionPane.showMessageDialog(null,"Uang Pembayaran Kurang!!");
} else if (jmlbayar>=jmlharga){
    kembalian.setText(Integer.toString(jmlbayar-jmlharga));
    String nambar=(String) namba.getText();
    int jumbel=Integer.parseInt(jbeli.getText());
    int jumbay=Integer.parseInt(jbayar.getText());
    int susuk=Integer.parseInt(kembalian.getText());
    JOptionPane.showMessageDialog(null,"    PESANAN BERHASIL\n\nNama Barang  = " +nambar+ "\nJumlah Beli     = " +jumbel+ "\nTotal Bayar      = " +jumbay+ "\nKembalian        = " +susuk);
}
        // TODO add your handling code here:
    }//GEN-LAST:event_bayarActionPerformed

